/**
 * Generates src/components/general/icon/icon.map.ts — a TypeScript map of
 * Phosphor icon names to their unicode codepoint characters.
 *
 * Parses Phosphor.svg to extract <glyph> entries with hex unicode codepoints.
 * All five font weights share identical codepoints, so only one SVG is needed.
 *
 * Run manually when Phosphor font files are updated:
 *   npm run generate:icon-map
 */

import { readFileSync, writeFileSync } from 'fs';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';

// eslint-disable-next-line @typescript-eslint/naming-convention
const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');

const svgPath = join(root, 'assets/fonts/phosphor/Phosphor.svg');
const svg = readFileSync(svgPath, 'utf-8');

// Match only glyphs with hex codepoints (skip ligature-string unicode entries and the space glyph)
const glyphRegex = /<glyph\s+unicode="&#x([0-9a-fA-F]+);"\s+glyph-name="([^"]+)"/g;

const entries = [];
let match;

while ((match = glyphRegex.exec(svg)) !== null) {
	const hex = match[1];
	const codepoint = parseInt(hex, 16);

	// Skip the space character
	if (codepoint === 0x20) continue;

	const names = match[2].split(',').map((n) => n.trim());

	for (const name of names) {
		entries.push([name, hex]);
	}
}

// Sort alphabetically for stable output
entries.sort((a, b) => a[0].localeCompare(b[0]));

// Build the TypeScript source
const lines = entries.map(([name, hex]) => `\t'${name}': '\\u{${hex}}'`);

const ts = `// Auto-generated by scripts/generate-icon-map.mjs — do not edit manually.
export const PHOSPHOR_ICON_MAP: Record<string, string> = {
${lines.join(',\n')}
};
`;

const outPath = join(root, 'src/components/general/icon/icon.map.ts');
writeFileSync(outPath, ts);

const uniqueCodepoints = new Set(entries.map(([, hex]) => hex)).size;
console.log(`Generated ${outPath}`);
console.log(`  ${uniqueCodepoints} unique codepoints, ${entries.length} total keys (including aliases)`);
